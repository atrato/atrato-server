public class AppPackageInfo
{
  private String owner;
  private String name;
  private String version;
  private String groupId;

  public String getOwner()
  {
    return owner;
  }

  public String getName()
  {
    return name;
  }

  public String getVersion()
  {
    return version;
  }

  public String getGroupId()
  {
    return groupId;
  }
}
